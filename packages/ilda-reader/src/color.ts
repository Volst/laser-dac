import { BlankingBit } from './file';

export function parseColor(st: any) {
  const colorIndex = (st >> 0) & 0x7f;
  const blanking = st & BlankingBit;
  const rgb = defaultColors[colorIndex % defaultColors.length];
  if (blanking || !rgb) {
    return { r: 0, g: 0, b: 0 };
  }
  return rgb;
}

